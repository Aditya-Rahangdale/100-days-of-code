public class Dsaj3 {
    public static void main(String[] args) {
        // type casting in java
        // typecasting is the best features of java to use these
        // narrowing extending
        int a=257;
        byte b=(byte) a;
        System.out.println(b);

        float d=50.67f;
        System.out.println(a*d);
        int e=(int)d;
        System.out.println(e);

    }
}
